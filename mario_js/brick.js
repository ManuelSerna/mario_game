//=====================================================================================================================
// Brick "class"
//=====================================================================================================================
class Brick extends Sprite
{
    constructor(startX, startY, startWidth, startHeight)
    {
        super(startX, startY, startWidth, startHeight);

        this.image1 = new Image();
        this.image1.src = "brick.png";

        this.type = "brick";
    }

    //=================================================================================================================
    // Update brick position
    //=================================================================================================================
    update()
    {
        this.scrollPos = masterScrollPos;
    }

    //=================================================================================================================
    // Draw the brick
    //=================================================================================================================
    draw(context)
    {
        context.drawImage(this.image1, this.xPos - masterScrollPos, this.yPos, this.width, this.height);
    }
}